		$sHeader = $this->PageHeader;
		$this->Page_DataRendering($sHeader);
		$Doc->Text .= $sHeader;
		$this->ExportDocument($Doc, $rs, $this->StartRec, $this->StopRec, "view");

		// Export detail records (actividad)
		if (EW_EXPORT_DETAIL_RECORDS && in_array("actividad", explode(",", $this->getCurrentDetailTable()))) {
			global $actividad;
			if (!isset($actividad)) $actividad = new cactividad;
			$rsdetail = $actividad->LoadRs($actividad->GetDetailFilter()); // Load detail records
			if ($rsdetail && !$rsdetail->EOF) {
				$ExportStyle = $Doc->Style;
				$Doc->SetStyle("h"); // Change to horizontal
				if ($this->Export <> "csv" || EW_EXPORT_DETAIL_RECORDS_FOR_CSV) {
					$Doc->ExportEmptyRow();
					$detailcnt = $rsdetail->RecordCount();
					$actividad->ExportDocument($Doc, $rsdetail, 1, $detailcnt);
				}
				$Doc->SetStyle($ExportStyle); // Restore
				$rsdetail->Close();
			}
		}
		$sFooter = $this->PageFooter;
		$this->Page_DataRendered($sFooter);
		$Doc->Text .= $sFooter;

		// Close recordset
		$rs->Close();

		// Call Page Exported server event
		$this->Page_Exported();

		// Export header and footer
		$Doc->ExportHeaderAndFooter();

		// Clean output buffer
		if (!EW_DEBUG_ENABLED && ob_get_length())
			ob_end_clean();

		// Write debug message if enabled
		if (EW_DEBUG_ENABLED && $this->Export <> "pdf")
			echo ew_DebugMsg();

		// Output data
		$Doc->Export();
	}

	// Set up master/detail based on QueryString
	function SetUpMasterParms() {
		$bValidMaster = FALSE;

		// Get the keys for master table
		if (isset($_GET[EW_TABLE_SHOW_MASTER])) {
			$sMasterTblVar = $_GET[EW_TABLE_SHOW_MASTER];
			if ($sMasterTblVar == "") {
				$bValidMaster = TRUE;
				$this->DbMasterFilter = "";
				$this->DbDetailFilter = "";
			}
			if ($sMasterTblVar == "objetivo") {
				$bValidMaster = TRUE;
				if (@$_GET["fk_idObjetivo"] <> "") {
					$GLOBALS["objetivo"]->idObjetivo->setQueryStringValue($_GET["fk_idObjetivo"]);
					$this->objetivo->setQueryStringValue($GLOBALS["objetivo"]->idObjetivo->QueryStringValue);
					$this->objetivo->setSessionValue($this->objetivo->QueryStringValue);
					if (!is_numeric($GLOBALS["objetivo"]->idObjetivo->QueryStringValue)) $bValidMaster = FALSE;
				} else {
					$bValidMaster = FALSE;
				}
			}
		} elseif (isset($_POST[EW_TABLE_SHOW_MASTER])) {
			$sMasterTblVar = $_POST[EW_TABLE_SHOW_MASTER];
			if ($sMasterTblVar == "") {
				$bValidMaster = TRUE;
				$this->DbMasterFilter = "";
				$this->DbDetailFilter = "";
			}
			if ($sMasterTblVar == "objetivo") {
				$bValidMaster = TRUE;
				if (@$_POST["fk_idObjetivo"] <> "") {
					$GLOBALS["objetivo"]->idObjetivo->setFormValue($_POST["fk_idObjetivo"]);
					$this->objetivo->setFormValue($GLOBALS["objetivo"]->idObjetivo->FormValue);
					$this->objetivo->setSessionValue($this->objetivo->FormValue);
					if (!is_numeric($GLOBALS["objetivo"]->idObjetivo->FormValue)) $bValidMaster = FALSE;
				} else {
					$bValidMaster = FALSE;
				}
			}
		}
		if ($bValidMaster) {

			// Save current master table
			$this->setCurrentMasterTable($sMasterTblVar);
			$this->setSessionWhere($this->GetDetailFilter());

			// Reset start record counter (new master key)
			$this->StartRec = 1;
			$this->setStartRecordNumber($this->StartRec);

			// Clear previous master key from Session
			if ($sMasterTblVar <> "objetivo") {
				if ($this->objetivo->CurrentValue == "") $this->objetivo->setSessionValue("");
			}
		}
		$this->DbMasterFilter = $this->GetMasterFilter(); // Get master filter
		$this->DbDetailFilter = $this->GetDetailFilter(); // Get detail filter
	}

